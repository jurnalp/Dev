package com.tutorial;

public class Main {

    public static void main(String[] args) {
        // operator komparasi.. menghasilkan nilai dalam bentuk boolean
        int i,j;
        boolean hasil;

        // operator equal / persamaaan
        i=79;
        j=79;
        hasil=(i==j);
        System.out.printf("%d == %d -->%s\n",i,j,hasil);

        i=79;
        j=79;
        hasil=(i!=j);
        System.out.printf("%d != %d -->%s\n",i,j,hasil);

        // <, >, <=, >=
    }
}
