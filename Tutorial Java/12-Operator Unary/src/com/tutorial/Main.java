package com.tutorial;

public class Main {

    public static void main(String[] args) {
        // unary = operasi yang dilakukan pada satu variable

        // unary + -
        int angka=1;
        System.out.printf("unary '-',%d menjadi %d\n",angka,-angka);
        System.out.printf("unary '+',%d menjadi %d\n",angka,+angka);

        //increment decrement
        int angka2=20;
        angka2++;
        System.out.printf("unary '++' menjadi %d\n",angka2);

        int angka3=30;
        angka3--;
        System.out.printf("unary '--',menjadi %d\n",angka3);

        // postfix dan prefix ++a ,a++

        // negasi boolean = !


    }
}
